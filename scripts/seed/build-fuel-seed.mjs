// Build a reviewable ค่าน้ำมัน (fuel) expense seed from the two source CSVs.
//   node scripts/seed/build-fuel-seed.mjs
// Mixer trucks come from fuel-mixer.csv (with odometer); non-mixer from
// fuel-nonmixer.csv. Ditto dates (blank date cell) carry forward. Exact
// duplicate rows in the mixer file (source copy-paste blocks) are dropped.
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const HERE = dirname(fileURLToPath(import.meta.url))
const ROOT = join(HERE, '..', '..')

/** Vehicle → { id, site }. Mixer plates + the three non-mixer tokens. */
const VEHICLE = {
  '80-6158': { id: '001', site: 'แพล้นปูน' },
  '80-5298': { id: '002', site: 'แพล้นปูน' },
  '80-6288': { id: '003', site: 'แพล้นปูน' },
  '80-5664': { id: '004', site: 'แพล้นปูน' },
  'loader': { id: 'loader', site: 'แพล้นปูน' },
  'pickup-6262': { id: 'pickup-6262', site: 'โรงหล่อ' },
  'forklift': { id: 'forklift', site: 'โรงหล่อ' },
}
/** Normalise obvious source typos on mixer plates. */
function normPlate(p) {
  let s = p.trim().replace(/\.+$/, '') // strip trailing dot (80-6158.)
  if (s === '80-5644') s = '80-5664'   // transposed digits
  return s
}
/** dd/mm/yy(พ.ศ. 2-digit) → ISO yyyy-mm-dd. 68→2568→2025, 69→2569→2026. */
function toIso(d) {
  const [dd, mm, yy] = d.split('/').map((x) => x.trim())
  const greg = 2500 + Number(yy) - 543
  return `${greg}-${String(Number(mm)).padStart(2, '0')}-${String(Number(dd)).padStart(2, '0')}`
}
const r2 = (n) => Math.round(n * 100) / 100
function splitCsv(line) {
  // minimal CSV: handles quoted fields with escaped quotes
  const out = []; let cur = ''; let q = false
  for (let i = 0; i < line.length; i++) {
    const c = line[i]
    if (q) { if (c === '"') { if (line[i + 1] === '"') { cur += '"'; i++ } else q = false } else cur += c }
    else if (c === '"') q = true
    else if (c === ',') { out.push(cur); cur = '' }
    else cur += c
  }
  out.push(cur)
  return out.map((s) => s.trim())
}
const rows = (file) => readFileSync(join(HERE, file), 'utf8').split(/\r?\n/).filter((l) => l.trim()).slice(1).map(splitCsv)

const warnings = []
const records = []

// ── Mixer file (with odometer + ditto dates + dedup) ──
let lastDate = ''
const seen = new Set()
let dupDropped = 0
for (const cols of rows('fuel-mixer.csv')) {
  const [plateRaw, dateRaw, litersRaw, priceRaw, amountRaw, odoRaw] = cols
  const plate = normPlate(plateRaw)
  const veh = VEHICLE[plate]
  if (!veh) { warnings.push(`mixer: unknown plate "${plateRaw}" (date ${dateRaw || lastDate})`); continue }
  const date = dateRaw.trim() ? dateRaw.trim() : lastDate
  if (dateRaw.trim()) lastDate = date
  if (!date) { warnings.push(`mixer: row with no date for ${plate}`); continue }
  const key = [plate, date, litersRaw, priceRaw, amountRaw, odoRaw].join('|')
  if (seen.has(key)) { dupDropped++; continue }
  seen.add(key)
  records.push({
    iso: toIso(date), vehId: veh.id, site: veh.site,
    liters: litersRaw ? Number(litersRaw) : undefined,
    price: priceRaw ? Number(priceRaw) : undefined,
    amount: Number(amountRaw),
    odometer: odoRaw && odoRaw.trim() ? Number(odoRaw) : undefined,
  })
}

// ── Non-mixer file (no odometer) ──
for (const cols of rows('fuel-nonmixer.csv')) {
  const [token, dateRaw, litersRaw, priceRaw, amountRaw] = cols
  const veh = VEHICLE[token]
  if (!veh) { warnings.push(`non-mixer: unknown token "${token}"`); continue }
  records.push({
    iso: toIso(dateRaw), vehId: veh.id, site: veh.site,
    liters: litersRaw ? Number(litersRaw) : undefined,
    price: priceRaw ? Number(priceRaw) : undefined,
    amount: Number(amountRaw),
    odometer: undefined,
  })
}

// ── Sort by date, then vehicle; assign stable ids ──
const VORDER = ['001', '002', '003', '004', 'loader', 'pickup-6262', 'forklift']
records.sort((a, b) => a.iso.localeCompare(b.iso) || VORDER.indexOf(a.vehId) - VORDER.indexOf(b.vehId))
const seed = records.map((r, i) => ({
  id: `ex_fuelseed_${String(i + 1).padStart(4, '0')}`,
  date: r.iso,
  category: 'ค่าน้ำมัน',
  site: r.site,
  amount: r2(r.amount),
  withVat: true,
  vehicleId: r.vehId,
  liters: r.liters != null ? r2(r.liters) : undefined,
  pricePerLiter: r.price != null ? r2(r.price) : undefined,
  odometer: r.odometer,
  createdBy: 'นำเข้า (seed)',
  createdAt: `${r.iso}T00:00:00.000Z`,
}))

// ── Emit the TS seed ──
const body = seed.map((r) => '  ' + JSON.stringify(r)).join(',\n')
const ts = `/* AUTO-GENERATED by scripts/seed/build-fuel-seed.mjs — do not edit by hand.
   Real ค่าน้ำมัน fuel records (Dec 2568 – Jun 2569), imported from the two source
   CSVs in scripts/seed/. Mixer trucks carry odometer; all are ลง VAT. */
import type { ExpenseRecord } from './createdDocs'

export const SEED_FUEL_EXPENSES: ExpenseRecord[] = [
${body},
]
`
writeFileSync(join(ROOT, 'src', 'data', 'seedFuelExpenses.ts'), ts)

// ── Report ──
const byVeh = {}
for (const r of seed) {
  const v = (byVeh[r.vehicleId] ??= { n: 0, liters: 0, baht: 0 })
  v.n++; v.liters += r.liters ?? 0; v.baht += r.amount
}
const totalBaht = seed.reduce((s, r) => s + r.amount, 0)
const dates = seed.map((r) => r.date).sort()
console.log('─'.repeat(60))
console.log(`SEED_FUEL_EXPENSES: ${seed.length} records`)
console.log(`date range: ${dates[0]} → ${dates[dates.length - 1]}`)
console.log(`mixer duplicate rows dropped: ${dupDropped}`)
console.log(`grand total: ${totalBaht.toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })} baht`)
console.log('─'.repeat(60))
console.log('per vehicle:  id            n   liters      baht')
for (const v of VORDER) {
  const d = byVeh[v]; if (!d) continue
  console.log(`  ${v.padEnd(14)} ${String(d.n).padStart(3)}  ${d.liters.toFixed(2).padStart(9)}  ${d.baht.toFixed(2).padStart(12)}`)
}
console.log('─'.repeat(60))
console.log(`warnings (${warnings.length}):`)
for (const w of warnings) console.log('  ! ' + w)
console.log('─'.repeat(60))
